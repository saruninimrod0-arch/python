# 1. Write a python program that is able to calculate the BMI of a person whose weight is 78kgs and height is 1.75 meters. BMI = (weight) / (height squared)
# Define variables
weight = 78  # in kg
height = 1.75  # in meters

# Calculate BMI
BMI = weight / (height ** 2)

# Print result
print("Your BMI is:", round(BMI, 2))

