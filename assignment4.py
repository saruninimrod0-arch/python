# Research on python functions both with parameters and without parameters.
# without parameters
def greet():
    print("Hello, World!")

greet()  # Output: Hello, World!


# without parameters
def greet(name):
    print(f"Hello, {name}!")

greet("Nimrod")  # Output: Hello, Nimrod!

# Using a for loop print from 2000 to 2024
for year in range(2000, 2025):
    print(year)

# Create a list of colurs Blue, Green, Pink, Black.Using a for loop.loop through the colours
colors = ["Blue", "Green", "Red", "Pink", "Black"]
for color in colors:
    print(color)
# Using a While Loop print from 20 to 1
num = 20
while num >= 1:
    print(num)
    num -= 1
